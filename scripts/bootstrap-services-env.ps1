# 从 deploy/testnet-v2.json + 当前 sui 活跃地址生成链下服务 .env.local（不提交 git）
param(
  [string]$DeployJson = "deploy/testnet-v2.json",
  [switch]$DryRunKeeper,
  [string]$CorsOrigin = "http://localhost:3000"
)

$ErrorActionPreference = "Stop"
$root = Split-Path $PSScriptRoot -Parent
Set-Location $root

$deployPath = Join-Path $root $DeployJson
if (-not (Test-Path $deployPath)) {
  throw "Missing $DeployJson"
}
$deploy = Get-Content $deployPath -Raw | ConvertFrom-Json

$active = (sui client active-address).Trim()
if ($active -ne $deploy.deployer) {
  Write-Warning "Active address $active != deployer $($deploy.deployer). Keeper must hold pool authority."
}

$keyJson = sui keytool export --key-identity $active --json | ConvertFrom-Json
$privKey = $keyJson.exportedPrivateKey
if (-not $privKey) { throw "Failed to export key for $active" }

$poolIds = @(
  $deploy.seedMarkets.poisson_goals.poolId,
  $deploy.seedMarkets.dirichlet_wdl.poolId,
  $deploy.seedMarkets.normal_cpi.poolId
) -join ","

$oracleConfig = $deploy.oracle.oracleConfigId
$appExamplePath = "app/.env.example"
if (-not $oracleConfig) {
  if (Test-Path $appExamplePath) {
    $oracleLine = Get-Content $appExamplePath | Where-Object { $_ -match "^NEXT_PUBLIC_ORACLE_CONFIG_ID=" } | Select-Object -First 1
    if ($oracleLine) { $oracleConfig = ($oracleLine -split "=", 2)[1].Trim() }
  }
}
if (-not $oracleConfig) { $oracleConfig = "" }

$keeperDry = if ($DryRunKeeper) { "true" } else { "false" }
$keeperProd = if ($DryRunKeeper) { "false" } else { "true" }

$keeperEnv = @"
# Auto-generated by bootstrap-services-env.ps1 — DO NOT COMMIT
SUI_NETWORK=testnet
SUI_RPC_URL=https://fullnode.testnet.sui.io:443
X_MARKET_PACKAGE_ID=$($deploy.packageId)
LP_GUARD_KEEPER_SECRET_KEY=$privKey
LP_GUARD_POOL_IDS=$poolIds
LP_GUARD_POLL_MS=30000
LP_GUARD_WINDOW_SAMPLES=10
LP_GUARD_MAX_EFFECTIVE_FEE_BPS=800
LP_GUARD_MAX_FEE_MULTIPLIER_BPS=30000
LP_GUARD_MAX_SIGMA_VIRTUAL_TENTHS=20
LP_GUARD_MAX_CONCENTRATION_VIRTUAL=50
LP_GUARD_DECAY_FACTOR=0.85
LP_GUARD_UPDATE_THRESHOLD_BPS=200
LP_GUARD_DRY_RUN=$keeperDry
LP_GUARD_PRODUCTION=$keeperProd
LP_GUARD_HEALTH_PORT=8788
HOST=0.0.0.0
"@

$monitorEnv = @"
# Auto-generated by bootstrap-services-env.ps1 — DO NOT COMMIT
SUI_NETWORK=testnet
SUI_RPC_URL=https://fullnode.testnet.sui.io:443
SUI_RPC_URL_FALLBACK=
