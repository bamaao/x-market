KEEPER_HEALTH_URL=http://localhost:8788/health
HOST=0.0.0.0
"@

$relayerEnv = @"
# Auto-generated by bootstrap-services-env.ps1 — DO NOT COMMIT
SUI_NETWORK=testnet
SUI_RPC_URL=https://fullnode.testnet.sui.io:443
SUI_RPC_URL_FALLBACK=
X_MARKET_PACKAGE_ID=$($deploy.packageId)
ORACLE_CONFIG_ID=$oracleConfig
ORACLE_POOL_IDS=$poolIds
ORACLE_RELAYER_POLL_MS=120000
ORACLE_RELAYER_HEALTH_PORT=8790
ORACLE_NULLIFY_HOURS=72
ALERT_WEBHOOK_URL=
HOST=0.0.0.0
"@

$walrusEnv = @"
# Auto-generated by bootstrap-services-env.ps1 — DO NOT COMMIT
WALRUS_UPSTREAM_PUBLISHER_URL=https://publisher.walrus-testnet.walrus.space
WALRUS_RELAY_PORT=8791
WALRUS_RELAY_API_KEY=
WALRUS_RELAY_MAX_BODY_BYTES=10485760
HOST=0.0.0.0
"@

$keeperPath = "services/lp-guard-keeper/.env.local"
$monitorPath = "services/chain-monitor/.env.local"
$relayerPath = "services/oracle-relayer/.env.local"
$walrusPath = "services/walrus-relay/.env.local"
$auditPath = "services/prophet-audit-keeper/.env.local"
$umaDvmPath = "services/uma-dvm-relayer/.env.local"
$zkProverPath = "services/brevis-zk-prover/.env.local"

$arbitratorId = $deploy.oracle.oracleArbitratorId
if (-not $arbitratorId -and (Test-Path "app/.env.local")) {
  $arbLine = Get-Content "app/.env.local" | Where-Object { $_ -match "^NEXT_PUBLIC_ORACLE_ARBITRATOR_ID=" } | Select-Object -First 1
  if ($arbLine) { $arbitratorId = ($arbLine -split "=", 2)[1].Trim() }
}

$umaDvmEnv = @"
# Auto-generated by bootstrap-services-env.ps1 — DO NOT COMMIT
SUI_NETWORK=testnet
SUI_RPC_URL=https://fullnode.testnet.sui.io:443
SUI_RPC_URL_FALLBACK=
X_MARKET_PACKAGE_ID=$($deploy.packageId)
