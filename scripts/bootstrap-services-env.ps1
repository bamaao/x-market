"@
Set-Content -Path $zkProverPath -Value $zkProverEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $zkProverPath -Value "`n"

# app/.env.local：基于 example + Gas Station URL
$appExample = "app/.env.example"
$appLocal = "app/.env.local"
if (Test-Path $appExample) {
  $appText = Get-Content $appExample -Raw
  if ($appText -notmatch "NEXT_PUBLIC_GAS_STATION_URL") {
    $appText += "`nNEXT_PUBLIC_GAS_STATION_URL=http://localhost:8787`n"
  } else {
    $appText = $appText -replace "NEXT_PUBLIC_GAS_STATION_URL=.*", "NEXT_PUBLIC_GAS_STATION_URL=http://localhost:8787"
  }
  if ($appText -notmatch "NEXT_PUBLIC_WALRUS_PUBLISHER_URL") {
    $appText += "NEXT_PUBLIC_WALRUS_PUBLISHER_URL=http://localhost:8791`n"
  } else {
    $appText = $appText -replace "NEXT_PUBLIC_WALRUS_PUBLISHER_URL=.*", "NEXT_PUBLIC_WALRUS_PUBLISHER_URL=http://localhost:8791"
  }
  Set-Content -Path $appLocal -Value $appText.TrimEnd() -Encoding UTF8
}

Write-Host "Wrote $gasPath"
Write-Host "Wrote $keeperPath"
Write-Host "Wrote $monitorPath"
Write-Host "Wrote $relayerPath"
Write-Host "Wrote $walrusPath"
Write-Host "Wrote $auditPath"
Write-Host "Wrote $umaDvmPath"
Write-Host "Wrote $zkProverPath"
if (-not $zkPolicyId) {
  Write-Warning "ZK_VERIFIER_POLICY_ID empty — run .\scripts\init-zk-verifier-policy.ps1 then re-bootstrap"
}
if (Test-Path $appLocal) { Write-Host "Wrote $appLocal" }
Write-Host ""
Write-Host "Keeper DRY_RUN=$keeperDry  PRODUCTION=$keeperProd"
Write-Host "Pools: $poolIds"
Write-Host "Next: .\scripts\start-services-testnet.ps1"
