"@
Set-Content -Path $keeperPath -Value $keeperEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $keeperPath -Value "`n"
Set-Content -Path $monitorPath -Value $monitorEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $monitorPath -Value "`n"
Set-Content -Path $relayerPath -Value $relayerEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $relayerPath -Value "`n"
Set-Content -Path $walrusPath -Value $walrusEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $walrusPath -Value "`n"
Set-Content -Path $auditPath -Value $auditEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $auditPath -Value "`n"
Set-Content -Path $umaDvmPath -Value $umaDvmEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $umaDvmPath -Value "`n"

$zkPolicyId = ""
if (Test-Path "app/.env.local") {
  $zkLine = Get-Content "app/.env.local" | Where-Object { $_ -match "^NEXT_PUBLIC_ZK_VERIFIER_POLICY_ID=" } | Select-Object -First 1
  if ($zkLine) { $zkPolicyId = ($zkLine -split "=", 2)[1].Trim() }
}

$zkProverEnv = @"
# Auto-generated by bootstrap-services-env.ps1 — DO NOT COMMIT
SUI_NETWORK=testnet
SUI_RPC_URL=https://fullnode.testnet.sui.io:443
SUI_RPC_URL_FALLBACK=
X_MARKET_PACKAGE_ID=$($deploy.packageId)
ZK_PROVER_SECRET_KEY=$privKey
ZK_PROVER_POOL_IDS=$poolIds
ZK_VERIFIER_POLICY_ID=$zkPolicyId
ZK_PROVER_POLL_MS=120000
ZK_PROVER_HEALTH_PORT=8794
ZK_PROVER_MODE=mock
ZK_PROVER_DRY_RUN=true
ZK_PROVER_MOCK_DELAY_MS=3000
BREVIS_RPC_URL=
BREVIS_PARTNER_KEY=
BREVIS_SRC_CHAIN_ID=101
ALERT_WEBHOOK_URL=
HOST=0.0.0.0
"@
Set-Content -Path $zkProverPath -Value $zkProverEnv.Trim() -Encoding UTF8 -NoNewline
Add-Content -Path $zkProverPath -Value "`n"

# app/.env.local：从 deploy json 同步（保留 example 中未列出的可选项）
$appLocal = "app/.env.local"
$circleUsdc = $deploy.usdc.coinType
if (-not $circleUsdc) {
  $circleUsdc = "0xa1ec7fc00a6f40db9693ad1415d0c193ad3906494428cf252621037bd7117e29::usdc::USDC"
}
$appEnv = @"
# Auto-synced from $DeployJson
NEXT_PUBLIC_PACKAGE_ID=$($deploy.packageId)
NEXT_PUBLIC_SUI_NETWORK=testnet
NEXT_PUBLIC_POOL_POISSON=$($deploy.seedMarkets.poisson_goals.poolId)
NEXT_PUBLIC_POOL_DIRICHLET=$($deploy.seedMarkets.dirichlet_wdl.poolId)
NEXT_PUBLIC_POOL_NORMAL=$($deploy.seedMarkets.normal_cpi.poolId)
NEXT_PUBLIC_USDC_COIN_TYPE=$circleUsdc
NEXT_PUBLIC_SUI_CLOCK=0x6
NEXT_PUBLIC_GLOBAL_CONFIG=$($deploy.globalConfig)
NEXT_PUBLIC_ADMIN_CAP=$($deploy.adminCap)
NEXT_PUBLIC_ORACLE_CONFIG_ID=$($deploy.oracle.oracleConfigId)
NEXT_PUBLIC_ORACLE_ARBITRATOR_ID=$($deploy.oracle.oracleArbitratorId)
NEXT_PUBLIC_PROPHET_REGISTRY_ID=$($deploy.prophet.registryId)
NEXT_PUBLIC_SEAL_THRESHOLD=1
NEXT_PUBLIC_INDEXER_URL=http://localhost:8800
NEXT_PUBLIC_PRICING_ENGINE_URL=http://localhost:8801
"@
Set-Content -Path $appLocal -Value $appEnv.Trim() -Encoding UTF8

Write-Host "Wrote $keeperPath"
Write-Host "Wrote $monitorPath"
Write-Host "Wrote $relayerPath"
Write-Host "Wrote $walrusPath"
Write-Host "Wrote $auditPath"
Write-Host "Wrote $umaDvmPath"
Write-Host "Wrote $zkProverPath"
if (-not $zkPolicyId) {
  Write-Warning "ZK_VERIFIER_POLICY_ID empty — run .\scripts\init-zk-verifier-policy.ps1 then re-bootstrap"
}
if (Test-Path $appLocal) { Write-Host "Wrote $appLocal" }
Write-Host ""
Write-Host "Keeper DRY_RUN=$keeperDry  PRODUCTION=$keeperProd"
Write-Host "Pools: $poolIds"
Write-Host "Next: .\scripts\start-services-testnet.ps1"
