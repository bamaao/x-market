HOST=0.0.0.0
"@

$gasPath = "services/gas-station/.env.local"
$keeperPath = "services/lp-guard-keeper/.env.local"
$monitorPath = "services/chain-monitor/.env.local"
$relayerPath = "services/oracle-relayer/.env.local"
$walrusPath = "services/walrus-relay/.env.local"
$auditPath = "services/prophet-audit-keeper/.env.local"
$umaDvmPath = "services/uma-dvm-relayer/.env.local"
$zkProverPath = "services/brevis-zk-prover/.env.local"

$arbitratorId = ""
if (Test-Path "app/.env.local") {
  $arbLine = Get-Content "app/.env.local" | Where-Object { $_ -match "^NEXT_PUBLIC_ORACLE_ARBITRATOR_ID=" } | Select-Object -First 1
  if ($arbLine) { $arbitratorId = ($arbLine -split "=", 2)[1].Trim() }
}

$umaDvmEnv = @"
